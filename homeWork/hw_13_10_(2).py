# Задание 1
# Для базы данных Больница, которую вы разрабатывали в рамках курса
# «Теория Баз Данных» создайте приложение для взаимодействия с базой данных.
# Этот интерфейс должен позволять:
# ■ Вставку строк в таблицы базы данных;
# ■ Обновление строк в таблицах базы данных. При попытке обновления всех строк
# в одной таблице нужно выдавать запрос на подтверждение пользователю.
# Обновлять все строки, можно только после положи-
# тельного подтверждения пользователем;
# ■ Удаление строк из таблиц баз данных. При попытке удаления всех строк
# в одной таблице нужно выдавать запрос на подтверждение пользователю.
# Удалять все строки, можно только после положительного подтверждения пользователем;
# ■ Создавать отчеты:
# • Вывести полные имена врачей и их специализации,
# • Вывести фамилии и зарплаты (сумма ставки и надбавки) врачей,
# которые не находятся в отпуске,
# • Вывести названия палат, которые находятся в конкретном отделении,
# • Вывести названия отделений без повторений, которые спонсируются конкретной компанией,
# • Вывести все пожертвования за указанный месяц в виде: отделение, спонсор,
# сумма пожертвования, дата пожертвования,
# • Вывести фамилии врачей с указанием отделений,
# в которых они проводят обследования.

